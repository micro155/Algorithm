package algorithm_220407;

import java.util.Scanner;

public class BOJ10951_AplusB_4_220407 {


    public static void main(String[] args) throws Exception {

        Scanner sc = new Scanner(System.in);

        while (sc.hasNextInt()) {

            int a = sc.nextInt();
            int b = sc.nextInt();

            System.out.println(a + b);
        }

        sc.close();
    }
}
